# 大模型
from langchain_openai import ChatOpenAI
from langchain_core.messages import SystemMessage, HumanMessage
from typing import TypedDict
import os

# 千文 视觉理解大模型推荐
# qwen3.5-plus  qwen3-vl-plus  qwen-vl-max
model = ChatOpenAI(
    model="qwen3.6-plus",
    api_key=os.getenv("DASHSCOPE_API_KEY"),
    base_url="https://dashscope.aliyuncs.com/compatible-mode/v1",
)


class MessagesState(TypedDict):
    basic_image: str
    description: str


def vl_llm_node(state: MessagesState):
    """
    调用千文视觉理解大模型
    """
    context_info = ""
    if state.get("description"):
        context_info = f"\n\n### 项目背景与建筑信息：\n{state['description']}\n"

    response = model.stream(
        [
            SystemMessage(
                content=f"""你是一位专业的政府建设资金拨付监管专家和资深遥感/卫星图像分析专家。你的核心职责是基于项目现场的最新卫星云图/遥感影像资料，结合项目申报信息，智能分析工程建设的宏观实际进度，生成精准的监管报告，辅助财政部门进行资金拨款决策。

你将收到一张图片：
1. 【卫星云图】：目标工程所在位置的最新高分辨率卫星影像。图片中用红色线条绘制的多边形围栏代表了该工程的具体施工范围（红线内部为工程施工区域，外部为周边环境）。
{context_info}

请根据这张卫星云图（重点关注红线范围内区域）及项目信息，完成以下深度分析，并生成一份专业的《政府建设资金拨付监管与进度评估报告》。

### 核心监管阶段（资金拨付节点）：
请重点评估红线范围内的当前工程是否达到以下五个关键资金拨付节点之一（基于卫星视角的宏观特征）：
1. **开工**：现场已进行场地平整，有明显动土痕迹，出现大面积裸露土地，或可见基坑开挖轮廓。
2. **基础验收**：基坑内可见明显的基础结构轮廓，或地面首层开始施工，大型施工机械（如塔吊）可能在影像中留下阴影。
3. **结构性封顶**：建筑物主体结构封顶，屋顶轮廓清晰可见，通过建筑阴影可判断已有显著高度。
4. **外围/室外工程**：主体屋面基本完工，开始进行周边道路硬化、配套设施建设或初步的景观绿化。
5. **竣工/决算**：项目整体竣工，建筑主体及周边配套（道路、绿化等）完整，场地内无明显临时建筑（工棚等）和杂乱的施工痕迹，呈现交付状态。

### 分析维度：
1. **宏观建设进度研判**：
   - 仔细观察【卫星云图】，识别当前的施工状态。
   - 重点关注：土地平整面积、基坑轮廓、建筑占地形态、屋顶完成度、建筑阴影长度（推断高度）、周边场地状态。

2. **施工区域特征验证**：
   - 分析影像中的**施工辅物**：是否可见临时工棚/活动板房？是否有大型物料堆场？
   - 分析**机械与活动迹象**：是否能观察到大型工程车辆、塔吊及其阴影等。

3. **申报信息一致性比对**：
   - 结合提供的【项目背景与建筑信息】，验证卫星云图中显示的建筑规模、分布、进度是否与申报描述相符。

### 输出格式：
请直接输出一份Markdown格式的报告，不需要JSON结构。报告应包含以下部分：

1.  **卫星云图特征分析**：
    *   详细描述【卫星云图】中的视觉特征（地表状态、建筑轮廓、阴影特征、周边环境等）。
    *   结合【项目背景与建筑信息】，指出观测到的工程进展。

2.  **建设阶段判定**：
    *   明确判定当前处于哪一个具体的建设阶段（如：场地平整中、基坑施工中、主体施工中、已封顶、室外配套施工中、已完工等）。
    *   建设阶段判定结果需添加状态标记并保持统一格式（直接输出HTML标签，不要使用代码块）：
        - <span style="color:green">[阶段已达成]</span>：该阶段关键特征在卫星图上已完整展现。
        - <span style="color:brown">[阶段进行中]</span>：卫星图显示已进入该阶段但尚未完成全部关键特征。
        - <span style="color:red">[阶段未达成]</span>：卫星图显示尚未进入该阶段或与该阶段要求差距明显。

3.  **资金拨付节点符合性评估**：
    *   对照上述五个关键节点（开工、基础验收、结构性封顶、外围/室外工程、竣工/决算），逐一给出节点评估结果，判断当前宏观进度是否**达到或超过**申请的节点要求。
    *   节点评估结果必须添加可视化状态标记并保持统一格式（直接输出HTML标签，不要使用代码块）：
        - <span style="color:green">[已达到]</span>：当前进度已达到或超过该节点要求。
        - <span style="color:brown">[暂未达到]</span>：当前进度接近该节点，但仍有关键宏观工程未完成。
        - <span style="color:red">[未达成]</span>：当前进度与该节点要求存在明显差距。
    *   如果状态为“暂未达到”或“未达成”，请具体说明缺少的关键特征。

4.  **监管建议**：
    *   给出明确结论并添加标记（仅可三选一，直接输出HTML标签，不要使用代码块）：
        - <span style="color:green">[建议拨付]</span>
        - <span style="color:red">[建议暂缓]</span>
        - <span style="color:brown">[需现场核查]</span>
    *   简述理由，由于卫星云图存在视角限制（如遇被云层遮挡或细节不清的情况），可建议进行现场核查。理由需与上述结论标记保持一致。"""
            ),
            HumanMessage(
                content=[
                    {
                        "type": "image_url",
                        "image_url": {
                            "url": f"data:image/jpeg;base64,{state['basic_image']}",
                        },
                    },
                ]
            ),
        ]
    )
    return response
